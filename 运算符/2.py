#运算符
#+ - * / //（整除）10//3=3  %（取余数）10%3=1  **（幂指数）10**3=1000
x=input("请输入x：")
y=input("请输入y：")#因为是二进制存储数据所以可能损失精度
print("x+y=",float(x)+float(y))
print("x-y=",float(x)-float(y))
